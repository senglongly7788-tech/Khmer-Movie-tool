import streamlit as st
import google.generativeai as genai
import asyncio
import edge_tts
import os

# бһ‘бһ¶бһүбһҷбһҖ API Key бһ–бһёбһҖбһ¶бһҡбһҖбҹҶбһҺбһҸбҹӢбһҡбһ”бһҹбҹӢ Streamlit
gemini_key = st.secrets.get("GEMINI_API_KEY", "")

if gemini_key:
    genai.configure(api_key=gemini_key)
else:
    st.error("бһҹбһјбһҳбһҖбҹҶбһҺбһҸбҹӢ GEMINI_API_KEY бһ“бҹ…бһҖбҹ’бһ“бһ»бһ„ Streamlit Advanced Settings бһҡбһ”бһҹбҹӢбһўбҹ’бһ“бһҖбһҮбһ¶бһҳбһ»бһ“бһҹбһ·бһ“!")

async def text_to_speech_khmer(text, output_filename):
    voice = "km-KH-PisethNeural" # бһҹбҹҶбһЎбҹҒбһ„бһҒбҹ’бһҳбҹӮбһҡ (бһ–бһ·бһҹбһ·бһҠбҹ’бһӢ)
    communicate = edge_tts.Communicate(text, voice)
    await communicate.save(output_filename)

st.set_page_config(page_title="Khmer Narrator Tool", page_icon="рҹҺҷпёҸ")
st.title("рҹҺҷпёҸ Tool бһ”бһҖбһ”бҹ’бһҡбҹӮ бһ“бһ·бһ„бһ”бһүбҹ’бһ…бһјбһӣбһҹбҹҶбһЎбҹҒбһ„бһҒбҹ’бһҳбҹӮбһҡбһўбһјбһҸбһј")
st.write("бһ”бһүбҹ’бһ…бһјбһӣбһўбһҸбҹ’бһҗбһ”бһ‘бһҡбһҝбһ„бһҮбһ¶бһ—бһ¶бһҹбһ¶бһ”бһҡбһ‘бҹҒбһҹ бһҠбһҫбһҳбҹ’бһ”бһёбһ”бһҖбһ”бҹ’бһҡбҹӮ бһ“бһ·бһ„бһ”бһ„бҹ’бһҖбһҫбһҸбһҮбһ¶бһҹбҹҶбһЎбҹҒбһ„бһҹбһҳбҹ’бһҡбһ¶бһҷбһҡбһҝбһ„бһҮбһ¶бһ—бһ¶бһҹбһ¶бһҒбҹ’бһҳбҹӮбһҡбҹ”")

original_script = st.text_area("бһ”бһүбҹ’бһ…бһјбһӣ Script бһҠбһҫбһҳ (бһўбһ„бҹӢбһӮбҹ’бһӣбҹҒбһҹ бһ…бһ·бһ“ бһҗбҹғ...):", height=200)

if st.button("рҹҺ¬ бһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳбһ’бҹ’бһңбһҫбһҖбһ¶бһҡ"):
    if not gemini_key:
        st.error("бһҳбһ·бһ“бһўбһ¶бһ…бһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡбһ”бһ¶бһ“бһ‘бҹҒ бһ–бҹ’бһҡбҹ„бҹҮбһҳбһ·бһ“бһ‘бһ¶бһ“бҹӢбһҳбһ¶бһ“ API Key бһ…бҹ’бһ”бһ¶бһҹбҹӢбһӣбһ¶бһҹбҹӢбҹ”")
    elif original_script.strip() == "":
        st.warning("бһҹбһјбһҳбһ”бһүбҹ’бһ…бһјбһӣ Script бһҮбһ¶бһҳбһ»бһ“бһҹбһ·бһ“!")
    else:
        with st.spinner("бһҖбҹҶбһ–бһ»бһ„бһ”бһҖбһ”бҹ’бһҡбҹӮбһҮбһ¶бһ—бһ¶бһҹбһ¶бһҒбҹ’бһҳбҹӮбһҡ..."):
            try:
                model = genai.GenerativeModel("gemini-1.5-flash")
                prompt = f"""
                бһ…бһјбһӣбһ”бһҖбһ”бҹ’бһҡбҹӮ Script бһҒбһ¶бһ„бһҖбҹ’бһҡбҹ„бһҳбһ“бҹҒбҹҮбһ‘бҹ…бһҮбһ¶бһ—бһ¶бһҹбһ¶бһҒбҹ’бһҳбҹӮбһҡ бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһҷбһҖбһ‘бҹ…бһўбһ¶бһ“бһ”бһүбҹ’бһ…бһјбһӣбһЎбһ¶бһ“бһҖбҹ’бһ“бһ»бһ„бһңбһёбһҠбҹҒбһўбһјбһҹбһҳбҹ’бһҡбһ¶бһҷбһҡбһҝбһ„бҹ”
                бһҹбһјбһҳбһ”бҹ’бһҡбһҫбһ”бҹ’бһҡбһ¶бһҹбҹӢбһ–бһ¶бһҖбҹ’бһҷбһ–бҹҒбһ…бһ“бҹҚбһұбҹ’бһҷбһҹбһҳбһҡбһҳбҹ’бһҷ бһ„бһ¶бһҷбһҹбҹ’бһҸбһ¶бһ”бҹӢ бһ бһјбһҡбһ бҹӮбһӣбҹ’бһў бһ“бһ·бһ„бһҖбһ»бҹҶбһ”бҹ’бһҡбһҫбһ–бһ¶бһҖбҹ’бһҷбһ”бһ…бҹ’бһӮбҹҒбһҖбһ‘бҹҒбһҹбһ–бҹҒбһҖбҹ”
                (бһ”бһүбҹ’бһҮбһ¶бһҖбҹӢбҹ– бһҶбҹ’бһӣбһҫбһҷбһҸбһ”бһҳбһҖбһңбһ·бһүбһҸбҹӮбһўбһҸбҹ’бһҗбһ”бһ‘бһҠбҹӮбһӣбһ”бһҖбһ”бҹ’бһҡбҹӮбһҡбһҪбһ…бһ”бҹүбһ»бһҺбҹ’бһҺбҹ„бҹҮ бһҳбһ·бһ“бһ”бһ¶бһ…бҹӢбһҳбһ¶бһ“бһ–бһ¶бһҖбҹ’бһҷбһ•бҹ’бһҸбһҫбһҳ бһ¬бһ–бһ¶бһҖбҹ’бһҷбһ–бһ“бҹ’бһҷбһӣбҹӢбһЎбһҫбһҷ)бҹ”
                
                Script:
                {original_script}
                """
                response = model.generate_content(prompt)
                khmer_text = response.text
                
                st.subheader("рҹ“қ бһўбһҸбҹ’бһҗбһ”бһ‘бһ”бһҖбһ”бҹ’бһҡбҹӮбһҮбһ¶бһҒбҹ’бһҳбҹӮбһҡбҹ–")
                st.write(khmer_text)
                
                st.info("бһҖбҹҶбһ–бһ»бһ„бһ”бҹҶбһ”бҹ’бһӣбҹӮбһ„бһ‘бҹ…бһҮбһ¶бһ бҹ’бһңбһ¶бһҷбһҹбҹҶбһЎбҹҒбһ„бһҒбҹ’бһҳбҹӮбһҡ...")
                audio_file = "khmer_voice.mp3"
                
                # бһҡбһҸбҹӢ TTS
                asyncio.run(text_to_speech_khmer(khmer_text, audio_file))
                
                st.success("бһҡбһҪбһ…бһҡбһ¶бһӣбҹӢбһ бһҫбһҷ!")
                st.subheader("рҹҺ§ бһ бҹ’бһңбһ¶бһҷбһҹбҹҶбһЎбҹҒбһ„бһҹбһҳбҹ’бһҡбһ¶бһҷбһҡбһҝбһ„бҹ–")
                st.audio(audio_file, format="audio/mp3")
                
                with open(audio_file, "rb") as file:
                    st.download_button(
                        label="рҹ“Ҙ бһ‘бһ¶бһүбһҷбһҖбһ бҹ’бһңбһ¶бһҷбһҹбҹҶбһЎбҹҒбһ„ (Download MP3)",
                        data=file,
                        file_name="khmer_narration.mp3",
                        mime="audio/mp3"
                    )
            except Exception as e:
                st.error(f"бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶бһҒбһ»бһҹбһ”бһ…бҹ’бһ…бҹҒбһҖбһ‘бҹҒбһҹбҹ– {e}")
